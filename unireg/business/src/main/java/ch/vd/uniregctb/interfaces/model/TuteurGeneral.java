package ch.vd.uniregctb.interfaces.model;

public interface TuteurGeneral {

    /**
     * Retourne le nom de la personne de contact du tuteur général.
     *
     * @return le nom de la personne de contact du tuteur général.
     */
    String getNomContact();

    /**
     * Retourne le nom de l'office du tuteur général.
     *
     * @return le nom de l'office du tuteur général.
     */
    String getNomOffice();

    /**
     * Retourne le numéro de téléphone de la personne de contact du tuteur général.
     *
     * @return le numéro de téléphone de la personne de contact du tuteur général.
     */
    String getNoTelephoneContact();

    /**
     * Retourne le prénom de la personne de contact du tuteur général.
     *
     * @return le prénom de la personne de contact du tuteur général.
     */
    String getPrenomContact();
}
