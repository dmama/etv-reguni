package ch.vd.uniregctb.evenement.di;

public class EvenementDeclarationException extends Exception {


	public EvenementDeclarationException() {
	}

	public EvenementDeclarationException(String message) {
		super(message);
	}

	public EvenementDeclarationException(String message, Throwable cause) {
		super(message, cause);
	}

	public EvenementDeclarationException(Throwable cause) {
		super(cause);
	}
}
