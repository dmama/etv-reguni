package ch.vd.uniregctb.admin.evenementExterne;

import java.util.Date;

public class CivilUnitaireView {

	private String noTechnique;
	private String noIndividu;
	private String numeroOFS;
	private Date dateEvenement;
	private Date dateTraitement;
	private String typeEvenementCivil;
	/**
	 * @return the noTechnique
	 */
	public String getNoTechnique() {
		return noTechnique;
	}
	/**
	 * @param noTechnique the noTechnique to set
	 */
	public void setNoTechnique(String noTechnique) {
		this.noTechnique = noTechnique;
	}
	/**
	 * @return the noIndividu
	 */
	public String getNoIndividu() {
		return noIndividu;
	}
	/**
	 * @param noIndividu the noIndividu to set
	 */
	public void setNoIndividu(String noIndividu) {
		this.noIndividu = noIndividu;
	}
	/**
	 * @return the numeroOFS
	 */
	public String getNumeroOFS() {
		return numeroOFS;
	}
	/**
	 * @param numeroOFS the numeroOFS to set
	 */
	public void setNumeroOFS(String numeroOFS) {
		this.numeroOFS = numeroOFS;
	}
	/**
	 * @return the dateEvenement
	 */
	public Date getDateEvenement() {
		return dateEvenement;
	}
	/**
	 * @param dateEvenement the dateEvenement to set
	 */
	public void setDateEvenement(Date dateEvenement) {
		this.dateEvenement = dateEvenement;
	}
	/**
	 * @return the dateTraitement
	 */
	public Date getDateTraitement() {
		return dateTraitement;
	}
	/**
	 * @param dateTraitement the dateTraitement to set
	 */
	public void setDateTraitement(Date dateTraitement) {
		this.dateTraitement = dateTraitement;
	}
	/**
	 * @return the typeEvenementCivil
	 */
	public String getTypeEvenementCivil() {
		return typeEvenementCivil;
	}
	/**
	 * @param typeEvenementCivil the typeEvenementCivil to set
	 */
	public void setTypeEvenementCivil(String typeEvenementCivil) {
		this.typeEvenementCivil = typeEvenementCivil;
	}



}
