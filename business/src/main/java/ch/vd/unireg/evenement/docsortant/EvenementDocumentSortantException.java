package ch.vd.uniregctb.evenement.docsortant;

public class EvenementDocumentSortantException extends Exception {

	public EvenementDocumentSortantException(String message) {
		super(message);
	}

	public EvenementDocumentSortantException(String message, Throwable cause) {
		super(message, cause);
	}

	public EvenementDocumentSortantException(Throwable cause) {
		super(cause);
	}

	public EvenementDocumentSortantException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}
}
